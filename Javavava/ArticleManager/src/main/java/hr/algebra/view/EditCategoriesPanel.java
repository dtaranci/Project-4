    private void btnSetFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetFilterActionPerformed
        var letterForFilter = spSpinner.getValue().toString().toUpperCase();
        Predicate<Category> letterFilter = category -> category.getName().toUpperCase().startsWith(letterForFilter);
        try {
            model = new CategoryTableModel(repository.selectCategories().stream().filter(letterFilter).collect(Collectors.toList()));
            updateTable();
        } catch (Exception ex) {
            Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSetFilterActionPerformed

