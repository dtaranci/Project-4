    private void btnAddNewCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewCategoryActionPerformed
        
        if (!formValid()) {
            return;
        }
        
        else {
            String categoryNameTf = tfNewCategoryName.getText().trim();
            if (rbUpdateCategory.isSelected()) {

                try {
                    repository.updateCategory(selectedCategory.getId(), new Category(categoryNameTf));
                } catch (Exception ex) {
                    Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else if (rbAddNewCategory.isSelected()) {
                try {
                    repository.createCategory(new Category(categoryNameTf));
                } catch (Exception ex) {
                    Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            try {
                initTable();
            } catch (Exception ex) {
                Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddNewCategoryActionPerformed

    private void btnRemoveCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCategoryActionPerformed
        if (selectedCategory == null) {
            MessageUtils.showInformationMessage("Information", "Select a category first");
        }
        
        try {
            
            //implement number of articles in use for a particular category
            int count = repository.selectCategoryArticleCount(selectedCategory.getId());
            
            if (count != 0) {
                MessageUtils.showErrorMessage("Category deletion failed", selectedCategory.getName() + " is currently in use by: " + count + " article(s)");
                return;
            }
            
            repository.deleteCategory(selectedCategory.getId());
            
            clearForm();
            initTable();
            
        } catch (Exception ex) {
            Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRemoveCategoryActionPerformed

