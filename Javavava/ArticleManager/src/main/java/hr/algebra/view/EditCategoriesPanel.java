    private boolean formValid() {
        hideErrors(errorLabels);
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());
        }
        return ok;
    }
        
    public void showCategory() {
        int selectedRow = tbCategories.getSelectedRow();
        
        int rowId = tbCategories.convertRowIndexToModel(selectedRow);
        
        int id = (int) model.getValueAt(rowId, 0);
        
        Optional<Category> opt;
        try {
            
            opt = repository.selectCategory(id);
            if (opt.isPresent()) {
                selectedCategory = opt.get();
                fillForm(selectedCategory);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(EditCategoriesPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


