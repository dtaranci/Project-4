    private void clearListModels() {
        allAuthorsModel.clear();
        articleAuthorsModel.clear();
    }
    
    private void initFormLists() {

        clearListModels();
        
        allAuthors =  authorAddable.getAllAuthors();
        articleAuthors = authorAddable.getArticleAuthors();
        
        //to compare later
        articleAuthorsDefault = new HashSet<Author>(articleAuthors);
        
        //only show addables
        allAuthors.removeAll(articleAuthors);

    }
    
    private void updateFormLists() {
        clearListModels();
        allAuthorsModel.addAll(allAuthors);
        articleAuthorsModel.addAll(articleAuthors);
        lsDialogAllAuthors.setModel(allAuthorsModel);
        lsDialogArticleAuthors.setModel(articleAuthorsModel);
    }

    private void initDragAndDrop() {
        
        //source for dnd
        lsDialogAllAuthors.setDragEnabled(true);
        lsDialogAllAuthors.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        lsDialogAllAuthors.setTransferHandler(new ExportHandler());
        
        //target for dnd
        lsDialogArticleAuthors.setDropMode(DropMode.ON);
        lsDialogArticleAuthors.setTransferHandler(new ImportHandler());
    }

    private class ExportHandler extends TransferHandler {

        @Override
        public int getSourceActions(JComponent c) {
            return COPY;
        }

        @Override
        protected Transferable createTransferable(JComponent c) {
            return new AuthorTransferable(lsDialogAllAuthors.getSelectedValue());
        }
        
    }
    
    private class ImportHandler extends TransferHandler { 

        @Override
        public boolean canImport(TransferSupport support) {
            return support.isDataFlavorSupported(AuthorTransferable.AUTHOR_FLAVOR);
        }

        @Override
        public boolean importData(TransferSupport support) {
            Transferable transferable = support.getTransferable();
            try {
                Author data = (Author) transferable.getTransferData(AuthorTransferable.AUTHOR_FLAVOR);
                
                //operations
                articleAuthors.add(data);
                allAuthors.remove(data);
                updateFormLists();
                
            } catch (UnsupportedFlavorException | IOException ex) {
                Logger.getLogger(AddAuthorDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            return false;
        }
        
        
        
    }
    
