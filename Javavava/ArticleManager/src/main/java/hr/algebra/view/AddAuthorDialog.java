    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (!articleAuthors.equals(articleAuthorsDefault)) {
            Set<Author> temp = new HashSet<>(articleAuthors);
            temp.removeAll(articleAuthorsDefault);
            
            if (!temp.isEmpty()) {
                try {
                    authorAddable.addAuthors(temp);
                } catch (Exception e) {
                    MessageUtils.showErrorMessage("Dialog error", e.getMessage().toString());
                }
                this.dispose();
            }
        }
        else {
            this.dispose();
        }
        
    }//GEN-LAST:event_btnOkActionPerformed


    

